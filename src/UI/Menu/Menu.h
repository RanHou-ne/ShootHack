// ============================================================================
// 文件：src/UI/Menu/Menu.h
// 模块：UI 层 - 菜单主框架接口
// ============================================================================
// 【用途】
//   声明菜单系统的公共接口。
//   菜单系统负责：
//   - 每帧渲染完整的 ImGui 菜单界面
//   - 管理 Tab 切换和动画
//   - 调用各子面板的绘制函数
//   - 处理菜单开关（INSERT 键）
//
// ============================================================================
// 【菜单结构】
//
//   Render()
//   ├── 背景层（粒子/渐变/贴图）
//   ├── 主窗口框体
//   │   ├── 顶部标题栏（Logo + 用户名 + 搜索框）
//   │   ├── 左侧 Tab 按钮列表（可展开/收起）
//   │   └── 右侧内容区域（根据当前 Tab 切换）
//   │       ├── Tab 0: DrawAimbotTab()    自瞄页
//   │       ├── Tab 1: DrawESPTab()       视觉页
//   │       ├── Tab 2: DrawOverviewTab()  颜色页
//   │       ├── Tab 3: DrawMemoryTab()    内存页
//   │       ├── Tab 4: DrawSettingsTab()  设置页
//   │       └── Tab 5: DrawConfigTab()    配置页
//   └── Toast 通知层
//
// ============================================================================
// 【扩展指南】
//
//   ① 添加新的 Tab 页面：
//      a. 在本文件中声明新的 DrawXxxTab() 函数
//      b. 在 Menu.cpp 中实现函数体
//      c. 在 Render() 的 Tab 数组中添加新 Tab 名称
//      d. 在 Render() 的 if (tabs == N) 分支中调用新函数
//      e. 将 Tab 总数从 6 改为 7（或更多）
//
//   ② 添加新的子面板（在现有 Tab 内）：
//      a. 在本文件中声明 static void DrawXxxPane()（static 表示仅内部使用）
//      b. 在 Menu.cpp 中实现
//      c. 在对应 Tab 的 BeginChild/EndChild 块中调用
//
//   ③ 修改菜单外观：
//      - 修改 imgui/imgui_settings.h 中的颜色/样式常量
//      - 修改 Render() 中的背景绘制逻辑
//
// ============================================================================

#pragma once

#include "ImGui/imgui.h"
#include "Core/globals.h"

namespace Menu
{
    // ========================================================================
    // 菜单状态
    // ========================================================================

    /// 菜单是否打开。由 INSERT 键切换（见 DX11Hook.cpp 的 hkPresent）。
    /// true: 渲染完整菜单并接管部分输入。
    /// false: 不绘制菜单，游戏正常运行。
    extern bool IsMenuOpen;

    // ========================================================================
    // 生命周期
    // ========================================================================

    /// 菜单初始化（ImGui 资源就绪后调用一次）。
    void Initialize();

    /// 菜单关闭/卸载时调用，释放菜单层面资源。
    void Shutdown();

    // ========================================================================
    // 主渲染函数
    // ========================================================================

    /// 每帧调用一次（在 hkPresent 中调用）。
    /// 内部负责：背景绘制、Tab 切换动画、各子面板渲染、Toast 通知。
    void Render();

    // ========================================================================
    // Tab 页面绘制函数
    // ========================================================================
    // 以下函数只负责"某个页面内容"的绘制，由 Render() 调用。
    // 修改规则：
    //   - 新增控件：先在 MenuSwitches::ToggleState 加字段，再在这里绑定。
    //   - 复杂逻辑：放到 MenuFeatureStubs.cpp，避免堆在 UI 文件里。
    void DrawESPTab();        // 视觉/透视设置页（Tab 1）
    void DrawAimbotTab();     // 自瞄/瞄准设置页（Tab 0）
    void DrawOverviewTab();   // 颜色/外观设置页（Tab 2）
    void DrawSettingsTab();   // 全局设置页（Tab 4）
    void DrawConfigTab();     // 配置管理页（Tab 5）

    // ========================================================================
    // 工具函数
    // ========================================================================

    /// 切换菜单开关（IsMenuOpen 取反）。一般由键盘热键触发。
    void ToggleMenu();

    /// 绘制菜单顶部标题栏（Logo + 版本号）。
    void DrawMenuHeader();

} // namespace Menu
